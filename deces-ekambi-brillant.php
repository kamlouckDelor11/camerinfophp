                    <p>La musique camerounaise est en deuil. L'artite chanteur, musicien et guitariste <strong> Ekambi Brillant</strong> de son vrai nom <strong>Ekambi Ekambi Louis Brillant</strong> n'est plus de ce monde. Le <strong>Mot’a muenya</strong> ou homme célèbre en langue Douala est décédé le 12 Décembre 2022 dans la même ville des suites de maldie.</p>
                    <h2>Bibiographie</h2>
                    <p>Fils d'Ekambi Brillant, grand commerçant et promoteur culturel, il est originaire de Dibombari un village tout proche de Douala et de Ekambi Claire native de Djébalè. Celui qu'on surnomme Mot'a Muenya (l'homme important).Tout jeune, il passe son enfance avec ses grands parents maternels à Djébalè (banlieue de Douala) ; c'est là que naît sa passion pour la musique à l'écoute des piroguiers chantant au retour de leurs sortie en la mer.</p>
                    <p>En 1962, et contre toute attente car son père le voyait difficilement réussir dans les études, il est admis au concours d’entrée en 6e au Lycée Général-Leclerc de Yaoundé. C’est de là, sous le regard de monsieur Zane Daniel - professeur de musique d’origine française, qu’il y apprend la musique et particulièrement à jouer de la guitare.</p>
                    <p>En 1971, à l’âge de 23 ans, il arrête ses études, en classe de seconde et rejoint la ville de Douala où il intègre l’orchestre Les crack’s comme guitariste. Ceux-ci se produisent dans la boite de nuit Le Domino. Il se présente ensuite au concours de la musique lancé par l’Office de Radiodiffusion Télévision Française (ORTF) dont le jury était composé, entre autres, de Manu Dibango et Francis Bebey. Il en est le lauréat. C’est grâce à ce prix qu’il sort son premier disque 45 tours intitulé Jonguèlè la Ndolo qui enregistra 20 000 ventes</p>
                    <p class="catch"><span class="catch__title" style="font-weight: bold; color: #000;">Lire aussi :</span> <span class="catch__desc"><a href="deces-nkotti-francois.php" style="color:#0070b2; border-bottom:solid .1rem #0070b2 ">Le makossa est en deuil, Nkotti François est décédé.</a></span></p>
                    <p>Arrivé en France en janvier 1972, avec le soutien de Jean Dikoto Mandengué (bassiste camerounais), il fut produit par Philips/Phonogram et sort son second disque 45 tours qui fut également un succès avec 25000 ventes.
                    <p>En 1975, il rompt son contrat avec Philips/Phonogram et se lie à Slim Pezin avec qui il sort l’album Africa Oumba avec comme titre phare Elongui qui par la suite a été repris par plusieurs autres artistes tant africains qu’européens. Cet album enregistra un record d’environ 4 millions de vente (Disque de diamant). Suivront des collaborations avec Slim Pezin comme producteur pour les titres Soul Castel, Musunguédi.</p>
                    <p>De passage au Cameroun dans les années 1976, Ekambi Brillant crée son orchestre appelé Les Ebis (Ekambi Brillant Show) qu’il fait venir en France.</p> 
                    <p>Ekambi Brillant a à son actif près de 20 albums. Il a contribué à l’éclosion de plusieurs artistes aussi bien camerounais comme Marthe Zambo, Valéry Lobé, Aladji Touré qu’africains à l’instar de Cella Stella, Angélique Kidjo.</p>
